#!/usr/bin/env node

/**
 * Snippet Extraction Logic
 * 
 * Extract extractable code snippets from parsed AST.
 * Filters out small functions (<5 lines) and trivial code.
 * Extracts meaningful functions, classes, and configs.
 * Calculates snippet metrics and generates descriptions.
 */

const fs = require('fs');
const path = require('path');

/**
 * Filter out trivial snippets
 */
function isTrivialFunction(func) {
  // Skip anonymous functions
  if (func.isAnonymous) return true;
  
  // Skip very short functions
  const lineCount = func.endLine - func.line;
  if (lineCount < 5) return true;
  
  // Skip functions with no params or empty params
  if (!func.params || func.params.length === 0) return true;
  
  // Skip setter/getter only functions
  if (func.name.startsWith('get_') || func.name.startsWith('set_')) {
    if (!func.params || func.params.length === 0) return true;
  }
  
  // Skip functions with no params
  if (!func.params || func.params.length === 0) return true;
  
  // Skip common utility functions
  const utilityPatterns = ['__', '$', '_'];
  if (utilityPatterns.some(p => func.name.startsWith(p))) {
    return true;
  }
  
  return false;
}

/**
 * Generate description for a function
 */
function generateFunctionDescription(func, content) {
  // Extract JSDoc if available
  if (func.docComment) {
    return func.docComment.replace(/\*/g, '').trim();
  }
  
  // Generate basic description based on name and params
  const paramList = (func.params && func.params.length > 0) 
    ? func.params.map(p => {
        if (p?.type === 'Identifier') return p.name;
        if (p?.type === 'ObjectPattern') return '{...}';
        return 'arg';
      }).join(', ') 
    : 'no params';
  
  return `Extracted ${func.name} function with ${paramList}`;
}

/**
 * Calculate snippet metrics
 */
function calculateSnippetMetrics(func, content) {
  const lines = func.endLine - func.line + 1;
  
  // Calculate cyclomatic complexity (simplified)
  let complexity = 1;
  const complexityKeywords = ['if', 'for', 'while', 'switch', 'case', 'catch', '&&', '||', '?', ':'];
  const funcContent = content.split('\n').slice(func.line - 1, func.endLine);
  const funcText = funcContent.join('\n').toLowerCase();
  
  complexityKeywords.forEach(keyword => {
    const escapedKeyword = keyword.replace(/[.*+?^$[\]\\]/g, '\\$&');
    const regex = new RegExp(`\\b${escapedKeyword}\\b`, 'g');
    complexity += (funcText.match(regex) || []).length;
  });
  
  return {
    lines,
    complexity,
    estimatedTime: Math.max(1, Math.ceil(complexity * 0.5))
  };
}

/**
 * Extract meaningful snippets from AST
 */
function extractSnippets(ast, content, filePath) {
  const snippets = [];
  
  // Extract functions
  if (ast.body) {
    ast.body.forEach(statement => {
      if (statement.type === 'FunctionDeclaration' || statement.type === 'AsyncFunctionDeclaration') {
        const func = {
          type: 'function',
          name: statement.id?.name,
          isAnonymous: !statement.id,
          line: statement.loc?.start?.line,
          endLine: statement.loc?.end?.line,
          params: statement.params?.length,
          isAsync: statement.async,
          isGenerator: statement.generator,
          sourceFile: filePath,
          docComment: null // Would need to extract from comments
        };
        
        if (!isTrivialFunction(func)) {
          const metrics = calculateSnippetMetrics(func, content);
          const description = generateFunctionDescription(func, content);
          
          snippets.push({
            id: null, // Will be generated by DB
            type: 'function',
            name: func.name,
            language: 'javascript',
            sourceFile: filePath,
            sourceLine: func.line,
            content: content.split('\n').slice(func.line - 1, func.endLine).join('\n'),
            description,
            tags: ['function', func.name],
            dependencies: [],
            context: `Function defined at line ${func.line}`,
            metrics,
            crossProject: true,
            createdAt: new Date(),
            updatedAt: new Date()
          });
        }
      }
    });
  }
  
  return snippets;
}

/**
 * Main extraction function
 */
function extractAndProcess(filePath) {
  const content = fs.readFileSync(filePath, 'utf8');
  const ext = path.extname(filePath);
  
  // Parse AST (simplified - would use the same parser as ast-parser.js)
  let ast;
  try {
    const babylon = require('@babel/parser');
    const parserOptions = {
      sourceType: 'module',
      ecmaVersion: 2022,
      locations: true
    };
    
    if (ext === '.ts') {
      parserOptions.plugins = [['typescript', { isModule: true }]];
    }
    
    const fileNode = babylon.parse(content, parserOptions);
    ast = fileNode.program || fileNode;
  } catch (e) {
    console.error(`Error parsing ${filePath}: ${e.message}`);
    return [];
  }
  
  // Extract snippets
  const snippets = extractSnippets(ast, content, filePath);
  
  return snippets;
}

/**
 * Main execution
 */
function main() {
  const args = process.argv.slice(2);
  
  if (args.length === 0) {
    console.error('Usage: node scripts/snippet-extractor.js <file1> <file2> ...');
    console.error('Example: node scripts/snippet-extractor.js src/index.js src/utils.ts');
    process.exit(1);
  }
  
  console.log('📦 Snippet Extraction');
  console.log('='.repeat(50));
  
  let totalSnippets = 0;
  const allSnippets = [];
  
  for (const filePath of args) {
    try {
      const snippets = extractAndProcess(filePath);
      allSnippets.push(...snippets);
      totalSnippets += snippets.length;
      console.log(`✅ Extracted ${snippets.length} snippets from: ${filePath}`);
    } catch (e) {
      console.error(`Error processing ${filePath}: ${e.message}`);
    }
  }
  
  console.log('='.repeat(50));
  console.log(`📊 Total snippets extracted: ${totalSnippets}`);
  
  // Save results
  const outputPath = path.join(__dirname, 'extracted-snippets.json');
  fs.writeFileSync(outputPath, JSON.stringify(allSnippets, null, 2));
  console.log(`✅ Results saved to: ${outputPath}`);
  
  // Display first snippet
  if (allSnippets.length > 0) {
    console.log('\n📋 Sample snippet:');
    console.log(allSnippets[0]);
  }
}

main();
